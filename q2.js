// set up two variables to test
let a = 1;
let b = 2;

// check if a and b are equal, or a is less than b, or a is greater than b
if (a == b) {
    // alert if a and b are equal
    alert("a and b are equal");
} else if (a < b) {
    // alert if a is less than b
    alert("a is less than b");
} else {
    // alert if a is greater than b (process of elimination, as a is less than b and a is equal to b are both already tested)
    alert("a is greater than b");
}